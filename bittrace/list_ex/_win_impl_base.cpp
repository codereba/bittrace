LRESULT WindowImplBase::OnCreate(UINT uMsg, WPARAM wParam, LPARAM lParam, BOOL& bHandled)
{
	LONG styleValue = ::GetWindowLong(*this, GWL_STYLE);
	styleValue &= ~WS_CAPTION;
	::SetWindowLong(*this, GWL_STYLE, styleValue | WS_CLIPSIBLINGS | WS_CLIPCHILDREN);
	RECT rcClient;
	::GetClientRect(*this, &rcClient);
	::SetWindowPos(*this, NULL, rcClient.left, rcClient.top, rcClient.right - rcClient.left, \
		rcClient.bottom - rcClient.top, SWP_FRAMECHANGED);

	paint_manager.Init(m_hWnd);
	paint_manager.AddPreMessageFilter(this);

	CDialogBuilder builder;

	paint_manager.SetResourcePath(GetSkinFolder().c_str());

#if USE(ZIP_SKIN) 

#if defined(UNDER_CE)
	static bool resource_unzipped = false;
	if (!resource_unzipped)
	{
		resource_unzipped = true;
		paint_manager.SetResourceZip(kResourceSkinZipFileName);
		paint_manager.UnzipResource();
		paint_manager.SetResourceZip(_T(""));
	}
	tString tstrSkin = paint_manager.GetResourcePath();
	tstrSkin += GetSkinFile();
#else
	paint_manager.SetResourceZip(kResourceSkinZipFileName);
	tString tstrSkin = GetSkinFile();
#endif

#else
	tString tstrSkin; //= paint_manager_.GetResourcePath();
	tstrSkin = GetSkinFile();
#endif

	do 
	{
		CControlUI* pRoot = builder.Create(tstrSkin.c_str(), (UINT)0, this, &paint_manager); 
		if( pRoot == NULL )
		{
			ASSERT( FALSE ); 
			break; 
		}

		paint_manager.AttachDialog(pRoot);
		paint_manager.AddNotifier(this);
	}while( FALSE );

	Init();
	return 0;
}

